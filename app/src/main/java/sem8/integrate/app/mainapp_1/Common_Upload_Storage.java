package sem8.integrate.app.mainapp_1;

public class Common_Upload_Storage
{
    public String name;
    public String url;

    public Common_Upload_Storage() {}

    public Common_Upload_Storage(String name, String url)
    {
        this.name = name;
        this.url = url;
    }

    public String getName()
    {
        return name;
    }

    public String getUrl()
    {
        return url;
    }

}
